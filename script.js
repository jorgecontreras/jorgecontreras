let currentLang = 'es';

function setLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update Toggle Button Style
    const btnEs = document.getElementById('btn-es');
    const btnEn = document.getElementById('btn-en');
    
    if (lang === 'es') {
        btnEs.classList.add('font-bold', 'text-primary');
        btnEs.classList.remove('text-gray-400', 'font-medium');
        btnEn.classList.add('text-gray-400', 'font-medium');
        btnEn.classList.remove('font-bold', 'text-primary');
    } else {
        btnEn.classList.add('font-bold', 'text-primary');
        btnEn.classList.remove('text-gray-400', 'font-medium');
        btnEs.classList.add('text-gray-400', 'font-medium');
        btnEs.classList.remove('font-bold', 'text-primary');
    }
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Check for browser language or saved preference (optional, keeping simple for now)
    setLanguage('es'); 
});

